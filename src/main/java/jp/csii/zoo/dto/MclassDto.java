package jp.csii.zoo.dto;

public class MclassDto {

	// id
	private int id;
	// gradeId
	private int gradeId;
	// name
	private String name;

	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id セットする id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return gradeId
	 */
	public int getGradeId() {
		return gradeId;
	}

	/**
	 * @param gradeId セットする gradeId
	 */
	public void setGradeId(int gradeId) {
		this.gradeId = gradeId;
	}

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}

}
